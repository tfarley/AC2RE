using System;

namespace AC2RE.Definitions {

    // Enum tagVARFLAGS
    [Flags]
    public enum VarFlag : uint {
        NONE = 0,
        READONLY = 1 << 0, // VARFLAG_FREADONLY 0x00000001
        SOURCE = 1 << 1, // VARFLAG_FSOURCE 0x00000002
        BINDABLE = 1 << 2, // VARFLAG_FBINDABLE 0x00000004
        REQUESTEDIT = 1 << 3, // VARFLAG_FREQUESTEDIT 0x00000008
        DISPLAYBIND = 1 << 4, // VARFLAG_FDISPLAYBIND 0x00000010
        DEFAULTBIND = 1 << 5, // VARFLAG_FDEFAULTBIND 0x00000020
        HIDDEN = 1 << 6, // VARFLAG_FHIDDEN 0x00000040
        RESTRICTED = 1 << 7, // VARFLAG_FRESTRICTED 0x00000080
        DEFAULTCOLLELEM = 1 << 8, // VARFLAG_FDEFAULTCOLLELEM 0x00000100
        UIDEFAULT = 1 << 9, // VARFLAG_FUIDEFAULT 0x00000200
        NONBROWSABLE = 1 << 10, // VARFLAG_FNONBROWSABLE 0x00000400
        REPLACEABLE = 1 << 11, // VARFLAG_FREPLACEABLE 0x00000800
        IMMEDIATEBIND = 1 << 12, // VARFLAG_FIMMEDIATEBIND 0x00001000
    }
}
