using System;

namespace AC2RE.Definitions {

    // Enum tagFUNCFLAGS
    [Flags]
    public enum FuncFlag : uint {
        NONE = 0,
        RESTRICTED = 1 << 0, // FUNCFLAG_FRESTRICTED 0x00000001
        SOURCE = 1 << 1, // FUNCFLAG_FSOURCE 0x00000002
        BINDABLE = 1 << 2, // FUNCFLAG_FBINDABLE 0x00000004
        REQUESTEDIT = 1 << 3, // FUNCFLAG_FREQUESTEDIT 0x00000008
        DISPLAYBIND = 1 << 4, // FUNCFLAG_FDISPLAYBIND 0x00000010
        DEFAULTBIND = 1 << 5, // FUNCFLAG_FDEFAULTBIND 0x00000020
        HIDDEN = 1 << 6, // FUNCFLAG_FHIDDEN 0x00000040
        USESGETLASTERROR = 1 << 7, // FUNCFLAG_FUSESGETLASTERROR 0x00000080
        DEFAULTCOLLELEM = 1 << 8, // FUNCFLAG_FDEFAULTCOLLELEM 0x00000100
        UIDEFAULT = 1 << 9, //FUNCFLAG_FUIDEFAULT 0x00000200
        NONBROWSABLE = 1 << 10, // FUNCFLAG_FNONBROWSABLE 0x00000400
        REPLACEABLE = 1 << 11, // FUNCFLAG_FREPLACEABLE 0x00000800
        IMMEDIATEBIND = 1 << 12, // FUNCFLAG_FIMMEDIATEBIND 0x00001000
    }
}
