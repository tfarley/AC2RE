using System;

namespace AC2RE.Definitions {

    // Enum tagTYPEFLAGS
    [Flags]
    public enum TypeFlag : uint {
        NONE = 0,
        APPOBJECT = 1 << 0, // TYPEFLAG_FAPPOBJECT 0x00000001
        CANCREATE = 1 << 1, // TYPEFLAG_FCANCREATE 0x00000002
        LICENSED = 1 << 2, // TYPEFLAG_FLICENSED 0x00000004
        PREDECLID = 1 << 3, // TYPEFLAG_FPREDECLID 0x00000008
        HIDDEN = 1 << 4, // TYPEFLAG_FHIDDEN 0x00000010
        CONTROL = 1 << 5, // TYPEFLAG_FCONTROL 0x00000020
        DUAL = 1 << 6, // TYPEFLAG_FDUAL 0x00000040
        NONEXTENSIBLE = 1 << 7, // TYPEFLAG_FNONEXTENSIBLE 0x00000080
        OLEAUTOMATION = 1 << 8, // TYPEFLAG_FOLEAUTOMATION 0x00000100
        RESTRICTED = 1 << 9, // TYPEFLAG_FRESTRICTED 0x00000200
        AGGREGATABLE = 1 << 10, // TYPEFLAG_FAGGREGATABLE 0x00000400
        REPLACEABLE = 1 << 11, // TYPEFLAG_FREPLACEABLE 0x00000800
        DISPATCHABLE = 1 << 12, // TYPEFLAG_FDISPATCHABLE 0x00001000
        REVERSEBIND = 1 << 13, // TYPEFLAG_FREVERSEBIND 0x00002000
        PROXY = 1 << 14, // TYPEFLAG_FPROXY 0x00004000
    }
}
