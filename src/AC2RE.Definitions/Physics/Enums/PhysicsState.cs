using System;

namespace AC2RE.Definitions {

    // Enum PhysicsState
    [Flags]
    public enum PhysicsState : uint {
        NONE = 0,
        STATIC = 1 << 0, // STATIC_PS 0x00000001
        CANNOT_STAND_ON = 1 << 1, // CANNOT_STAND_ON_PS 0x00000002
        REPORT_COLLISIONS = 1 << 2, // REPORT_COLLISIONS_PS 0x00000004
        IGNORE_COLLISIONS = 1 << 3, // IGNORE_COLLISIONS_PS 0x00000008
        NODRAW = 1 << 4, // NODRAW_PS 0x00000010
        MISSILE = 1 << 5, // MISSILE_PS 0x00000020
        PUSHABLE = 1 << 6, // PUSHABLE_PS 0x00000040
        ALIGNPATH = 1 << 7, // ALIGNPATH_PS 0x00000080
        PATHCLIPPED = 1 << 8, // PATHCLIPPED_PS 0x00000100
        GRAVITY = 1 << 9, // GRAVITY_PS 0x00000200
        LIGHTING_ON = 1 << 10, // LIGHTING_ON_PS 0x00000400
        UNDETECTABLE = 1 << 11, // UNDETECTABLE_PS 0x00000800
        HAS_PHYSICS_BSP = 1 << 12, // HAS_PHYSICS_BSP_PS 0x00001000
        INELASTIC = 1 << 13, // INELASTIC_PS 0x00002000
        CLOAKED = 1 << 14, // CLOAKED_PS 0x00004000
        EDGE_SLIDE = 1 << 15, // EDGE_SLIDE_PS 0x00008000
        SLEDDING = 1 << 16, // SLEDDING_PS 0x00010000
        MOVEMENT_FROZEN = 1 << 17, // MOVEMENT_FROZEN_PS 0x00020000
        ANIMATION_FROZEN = 1 << 18, // ANIMATION_FROZEN_PS 0x00040000
        GAMEDRAW = 1 << 19, // GAMEDRAW_PS 0x00080000
        TOOLSDRAW = 1 << 20, // TOOLSDRAW_PS 0x00100000
        MOBILE = 1 << 21, // MOBILE_PS 0x00200000

        INWORKSPACE = 1 << 28, // INWORKSPACE_PS 0x10000000
        PHYSICSDISABLED = 1 << 29, // PHYSICSDISABLED_PS 0x20000000
        HAS_MATRIX_WEIGHTS = 1 << 30, // HAS_MATRIX_WEIGHTS_PS 0x40000000
        SYNC_TRANSIT = 1u << 31, // SYNC_TRANSIT_PS 0x80000000
    }
}
